public static class Problem96
{
    public static int[,] CreateGridFromString(string str)
    {
        var sanitizedString = str
            .Replace(Environment.NewLine, string.Empty)
            .Replace(" ", string.Empty);

        var grid = new int[9, 9];

        for (var index = 0; index < sanitizedString.Length; index++)
        {
            var value = int.Parse(sanitizedString[index].ToString());
            var i = index / 9;
            var j = index % 9;
            grid[i, j] = value;
        }

        return grid;
    }

    public static string CreateStringFromGrid(int[,] grid)
    {
        var sb = new StringBuilder();
        for (var index = 0; index < 81; index++)
        {
            var i = index / 9;
            var j = index % 9;

            if (index != 0 && j == 0)
                sb.Append(Environment.NewLine);

            sb.Append(grid[i, j]);
        }

        return sb.ToString();
    }

    private static IEnumerable<int> EmptyIndices(int[,] grid)
    {
        for (var index = 0; index < 81; index++)
        {
            var i = index / 9;
            var j = index % 9;

            if (grid[i, j] == 0)
                yield return index;
        }
    }

    private static bool IsValid(int[,] grid, int i, int j, int value)
    {
        for (var k = 0; k < 9; k++)
            if (grid[i, k] == value || grid[k, j] == value)
                return false;

        var startRow = i - i % 3;
        var startColumn = j - j % 3;

        for (var m = 0; m < 3; m++)
        for (var n = 0; n < 3; n++)
            if (grid[startRow + m, startColumn + n] == value)
                return false;

        return true;
    }

    public static int[,] SolveSuDoku(int[,] grid)
    {
        var index = 0;
        var emptyIndices = EmptyIndices(grid).ToArray();

        while (index != emptyIndices.Length)
        {
            var x = emptyIndices[index];
            var i = x / 9;
            var j = x % 9;

            for (var value = grid[i, j]; value <= 9; value++) // increment and check validity
            {
                if (IsValid(grid, i, j, value))
                {
                    grid[i, j] = value;
                    index++;
                    break;
                }

                if (value == 9)
                {
                    grid[i, j] = 0; // backtrack and reset
                    index--;
                    break;
                }
            }
        }

        return grid;
    }

    public static int Solution()
    {
        var solvedGrids = Data()
            .Select(SolveSuDoku);
        
        return solvedGrids
            .Select(grid =>
            {
                var str = grid[0, 0].ToString() + grid[0, 1].ToString() + grid[0, 2].ToString();
                var number = int.Parse(str);
                return number;
            })
            .Sum();
    }

